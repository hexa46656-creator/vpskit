#!/usr/bin/env bash

set -euo pipefail

PROJECT_ROOT="$(git -C "${BATS_TEST_DIRNAME}" rev-parse --show-toplevel)"

load_core() {
  source "${PROJECT_ROOT}/vpskit/core/common.sh"
  # shellcheck disable=SC1090
  source "${PROJECT_ROOT}/vpskit/core/$1"
}

