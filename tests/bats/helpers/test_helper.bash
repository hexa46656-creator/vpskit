  unset VPSKIT_PUBLIC_IPV4
  unset VPSKIT_HYSTERIA2_PORT
  unset VPSKIT_HYSTERIA2_MASQUERADE_HOST
  unset VPSKIT_LOCK_PATH
  unset VPSKIT_LOCK_METADATA_PATH
  unset VPSKIT_LOCK_FD
